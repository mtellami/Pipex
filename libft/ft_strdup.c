#include "pipex.h"

char	*ft_strdup(char *str)
{
	char	*s;
	int		i;

	s = malloc(ft_strlen(str) + 1);
	if (!s)
		return (0);
	i = 0;
	while (str[i])
	{
		s[i] = str[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
